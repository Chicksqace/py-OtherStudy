# 求数字的阶乘：
# 6的阶乘：6*5*4*3*2*1
# 3的阶乘：3*2*1
def 阶乘(传入的数字):
    结果=1
    while 传入的数字>0:
        结果 *=传入的数字
        传入的数字=传入的数字-1
    return 结果
print("阶乘3=",阶乘(3))
print("阶乘6=",阶乘(6))
print("阶乘12=",阶乘(12))
